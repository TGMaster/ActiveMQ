package Factorial;

/**
 *
 * @author TGMaster
 */
public class Number {
    private Integer number;
    private Integer result;

    public Number() {
    }

    public Number(Integer number, Integer result) {
        this.number = number;
        this.result = result;
    }

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }

    public Integer getResult() {
        return result;
    }

    public void setResult(Integer result) {
        this.result = result;
    }

        
}
